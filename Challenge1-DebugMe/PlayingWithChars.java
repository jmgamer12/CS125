
public class PlayingWithChars {
	public static void main(String[] args) {
		for (int iRow = 0; iRow <= 10; iRow++) {
			System.out.printf("%4d", iRow);
			for (int iCol = 0; iCol < 10; iCol++) {
				int index = 10 * iRow + iCol;
				System.out.printf("%5s", (char)index);
			}
			System.out.println();
		}
	}
}
